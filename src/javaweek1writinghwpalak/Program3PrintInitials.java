package javaweek1writinghwpalak;
/* 3. Write a program that will print your initials to standard output in letters that
 * are nine lines tall. Each big letter should be made up of a bunch of *'s. */

public class Program3PrintInitials {
    public static void main(String[] args) {
        System.out.println("*****   *       *****");
        System.out.println("*   *  * *      *    *");
        System.out.println("*   *  *  *     *     *");
        System.out.println("***** *    *    *     *");
        System.out.println("*     * *** *   *     *");
        System.out.println("*     *     *   *     *");
        System.out.println("*     *     *   *     *");
        System.out.println("*     *     *   *     *");
        System.out.println("*     *     *   ******");
    }
    }

